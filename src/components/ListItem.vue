<template>
  <option v-on:click="handleClick">{{ beer.name }}</option>
</template>

<script>
import { eventBus } from '../main.js'

export default {
    name: 'list-item',
    props: ['beer'],
    methods: {
        handleClick(){
            eventBus.$emit('beer-selected', this.beer)
        }
    }

}
</script>

